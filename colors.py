def get_rgb_color(color, alpha = None):
    """Takes color name and returns a list containing the RGB value for that color
    if alpha value between 0-255 is given it also appends the alpha value
    Examples:
    black = [0, 0, 0]
    black, 200 = [0,0,0,200]

    Args:
        color (string): Name of the color you would like to fetch
        alpha (int), optional): A 0-255 value. If this is provided the function returns the
        color plus the alpha value.. Defaults to None.

    Raises:
        TypeError: Color needs to be of type string.
        KeyError: If the color does not excist in the dictionary an error is raised.
        TypeError: The alpha value needs to be of type integer.
    """
    # Check that variable type
    if not isinstance(color, str):
        raise TypeError('color needs to be of type string')

    # Ensure color is lowercase
    if not color.islower():
        color = color.lower()

    # Check that the key exist
    if not color in rgb_dictionary:
        raise KeyError(f'''Tried to fetch the color: {color}. The color
         does not exist. Please consult the rgb spreadsheet.''')
    

    # Add alpha value if parameter is given.
    if alpha == None:
        alpha = []
    else:
        if not isinstance(alpha, int):
            raise TypeError('Alpha needs to be of type integer')
        elif alpha < 0:
            alpha = 0
        elif alpha > 255:
            alpha = 255
        alpha = [alpha]

    return(rgb_dictionary.get(color)+alpha)

def get_hex_color(color, alpha_percent = None):
    """Takes a color name and optionally an alpha value in percentage 0-100.
    Then returns the corresponding color in hexadecimal values. Color names
    are separated by underscores.
    Examples: light_gray, blue, navy

    Args:
        color (string): the color name separated by underscores
        alpha_percent (int/float, optional): alpha level 0-100%. Defaults to None.

    Raises:
        TypeError: the color needs to be a string value
        KeyError: the color given must be in the hex dictionary.
        ValueError: if alpha is given and is neither int or float. Raise an exception.

    Returns:
        string: returns the hex string useable with PILLOWs image from hexadecimal
    """
    # Check variable type
    if not isinstance(color, str):
        raise TypeError('The color needs to be a string')

    # Ensure color is lowercase
    if not color.islower():
        color = color.lower()

    if not color in hex_dictionary:
        raise KeyError(f'''Tried to fetch the color: {color}. The color
         does not exist. Please consult the hex dictionary.''')

    hex_alpha_string = ''
    if not alpha_percent == None:
        if not (isinstance(alpha_percent, float) or isinstance(alpha_percent, int)):
            raise ValueError('alpha_percent needs to be an integer or float between 0 and 100')

        # handle out of bounds values
        if alpha_percent < 0:
            print('alpha_percent should be 0 or greater. Changed the value to zero')
            alpha_percent = 0
        elif alpha_percent > 100:
            print('alpha_percent should be lower or equal to 100. Changed the value to 100')
            alpha_percent = 100

        # Can only covert integers to hexadecimal values)
        to_hex = int(255*alpha_percent/100)
        hex_alpha_string = hex_dictionary(to_hex)
        hex_alpha_string.replace('0x','')
    
    # Return the hex color valye with a alpha level if provided
    return (hex_dictionary.get(color)+hex_alpha_string)

